class Solution(object):
    def searchMatrix(self, matrix, target):
        """
        :type matrix: List[List[int]]
        :type target: int
        :rtype: bool
        """
        m = len(matrix)
        if m == 0:
            return False

        n = len(matrix[0])
        if n == 0:
            return False

        x = 0
        y = m - 1

        while x < n and y >= 0:
            v = matrix[y][x]
            if target == v:
                return True
            elif target > v:
                x += 1
            elif target < v:
                y -= 1

        return False


